//1. Introducción

//Esta Política de Privacidad describe cómo el bot Happysugarlife (en adelante, “el bot”) recopila, utiliza, almacena y protege la información de los usuarios dentro de la plataforma Discord.
//Al utilizar el bot, aceptas esta política en su totalidad. Si no estás de acuerdo con ella, por favor, deja de usar el bot.

//2. Información que recopilamos

//El bot almacena y procesa datos relacionados con la actividad del usuario en los servidores donde está activo, incluyendo, entre otros:

//ID de usuario, nombre de usuario y tag de Discord.

//ID del servidor y canales.

//Mensajes y contenido generado por el usuario (según las funciones del bot).

//Configuraciones, preferencias y datos generados por comandos.

//Cualquier otra información necesaria para el funcionamiento del bot.

//3. Uso de la información

//La información recopilada se utiliza para:

//Permitir el funcionamiento y las funciones principales del bot.

//Mejorar el rendimiento, estabilidad y experiencia de uso.

//Registrar configuraciones personalizadas por servidor o usuario.

//Prevenir abusos o comportamientos que violen los Términos de Uso de Discord.

//4. Almacenamiento y seguridad de los datos

//Toda la información recopilada es almacenada de forma segura en servidores o bases de datos controladas por el desarrollador del bot.
//Se aplican medidas razonables para proteger los datos frente a accesos no autorizados, pérdida o alteración.
//No obstante, el desarrollador no puede garantizar una seguridad absoluta debido a la naturaleza de los servicios en línea.

//5. Compartición de la información

//Los datos no se comparten con terceros, salvo en los siguientes casos:

//Cuando sea necesario para cumplir con la ley o una solicitud legal.

//Para mantener la seguridad e integridad del servicio.

//6. Retención y eliminación de datos

//Los datos se conservan mientras el bot esté activo o sea necesario para sus funciones.
//Si deseas eliminar tus datos o los de tu servidor, puedes solicitarlo contactando directamente con el desarrollador del bot.

